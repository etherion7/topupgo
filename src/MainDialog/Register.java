    public static boolean cekOTP(String otp){
        try
        {
            Main.mainConnectionDB.pst = Main.mainConnectionDB.con.prepareStatement("SELECT registCode FROM user WHERE email = ?");
            Main.mainConnectionDB.pst.setString(1, email.getText());
            Main.mainConnectionDB.rs = Main.mainConnectionDB.pst.executeQuery();
            String datadb = "";
            while(Main.mainConnectionDB.rs.next()){
                datadb = Main.mainConnectionDB.rs.getString("registCode");
            }
            
            if(otp.equals(datadb)){
                Main.mainConnectionDB.pst = Main.mainConnectionDB.con.prepareStatement("UPDATE user set userActivation = ?, registCode = ? where email =?");
                Main.mainConnectionDB.pst.setInt(1, 1);
                Main.mainConnectionDB.pst.setString(2, null);
                Main.mainConnectionDB.pst.setString(3, email.getText());
                Main.mainConnectionDB.pst.executeUpdate();
                System.out.println("update berhasil");
                return true;
            }else{
                System.out.println("otp beda, update gagal");
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
    
    public boolean cekUsername(String usernameRegist){
        String sqlcon = "select * from user";
                
        try{
            ResultSet cek = Main.mainConnectionDB.con.createStatement().executeQuery(sqlcon);
            while(cek.next()){
                if(usernameRegist.equals(cek.getString("username"))){
                    return true;
                }
            }
        }catch(SQLException ex){
            System.out.print(ex.toString());
        }
        return false;
    }
    
