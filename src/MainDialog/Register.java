    private void btnRegisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisActionPerformed
        if(username.getText().length() > 0){
            if(email.getText().length() > 0){
                if(password.getText().length() > 0 && repassword.getText().length() > 0){
                    if(password.getText().equalsIgnoreCase(repassword.getText())){
                        if(cekUsername(username.getText())){
                            JOptionPane.showMessageDialog(null, "Username ini telah digunakan",
                            "Warning", JOptionPane.ERROR_MESSAGE);
                            username.setText("");
                        }else{
                            if(cekEmail(email.getText())){
                            JOptionPane.showMessageDialog(null, "Email ini telah digunakan",
                            "Warning", JOptionPane.ERROR_MESSAGE);
                            email.setText("");
                            }else{
                                String salt = Main.passEncrypt.getSaltvalue(6);
                                String otp = Main.passEncrypt.generateOTP(6);
                                String sql = "insert into user(username, email, password, salt, userActivation, registCode) values (?, ?, ?, ?, ?, ?)";                           
                                try{ 
                                    Main.mainConnectionDB.pst = Main.mainConnectionDB.con.prepareStatement(sql);
                                    Main.mainConnectionDB.pst.setString(1, username.getText());
                                    Main.mainConnectionDB.pst.setString(2, email.getText());
                                    Main.mainConnectionDB.pst.setString(3, Main.passEncrypt.generateSecurePassword(password.getText(), salt));
                                    Main.mainConnectionDB.pst.setString(4, salt);//salt
                                    Main.mainConnectionDB.pst.setInt(5, 0);
                                    Main.mainConnectionDB.pst.setString(6, otp);
                                    Main.mainConnectionDB.pst.executeUpdate();
                                    Main.sendEmail.sendEmailRegist(email.getText(), otp);
                                    System.out.println("Insert data user berhasil !");
                                    JOptionPane.showMessageDialog(this, "Kode OTP telah dikirim ke email kamu");
                                    
                                }catch(HeadlessException | SQLException ex){
                                    System.out.println("Errornya adalah " + ex.getMessage());
                                }
                                
                                MainDialog.KodeOTP dialogOTP = new MainDialog.KodeOTP(null, true);
                                dialogOTP.setVisible(true);
                            }
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Password dan Confirm Password berbeda",
                "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
                        password.setText("");
                        repassword.setText("");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Kamu belum input password",
                "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Kamu belum input email",
                "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Kamu belum input username",
                "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisActionPerformed

